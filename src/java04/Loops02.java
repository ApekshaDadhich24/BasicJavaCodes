//print first 20 numbers using while loop
package java04;
import java.util.*;
public class Loops02 {
    public static void main(String[] args) {
        System.out.println("Printing numbers from 1-20:");
        int i=1;
        while (i<=20) {
            System.out.println(i);
            i=i+1;
        }
    }
}
